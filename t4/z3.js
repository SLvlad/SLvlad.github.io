let number = prompt(`Введіть будь-яке число`);
if(number > 0){
    alert(`Введене число є додатнім`)
}else if(number < 0){
    alert(`Введене число є від'ємним`)
}else if(number == 0){
    alert(`Введене число не є ні додатнім ні від'ємним`)
}else{
    alert(`Невірно введене число`)
}if(number % 2 == 0){
    alert(`Введене число є парним`) 
}else{
    alert(`Введене число непарне`)
}